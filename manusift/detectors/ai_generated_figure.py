"""AI-generated-figure detector (P0-AI).

A 2024-2025 retraction-wave category: papers retracted because
their figures were generated by an AI image model (Midjourney,
DALL-E, Stable Diffusion, ComfyUI, Firefly, etc.). The Frontiers
2024 AI-retraction cohort alone accounts for 100+ papers, and the
trend is accelerating into 2025+ (1,743 papers retracted in 2025
for suspected AI fabrication per a 2026-06-13 web search).

We cannot detect generative AI in arbitrary pixel data with 100%
reliability -- that requires a forensic-GAN-fingerprint model and
is outside the scope of this patch. Instead, we run three
**lightweight probes** that together produce actionable evidence:

1. **PDF metadata fingerprint** (high-signal when it fires):
   Open the PDF ``/Info`` dictionary + XMP packet and look for
   strings that match known AI-generation tools. Midjourney,
   Stable Diffusion, ComfyUI and Firefly all leave the producing
   software name in the metadata of the rendered PNG/JPG they
   export, which downstream tools often copy into the PDF.

2. **Producer / Creator chain anomaly** (medium-signal):
   If the metadata contains an AI-prompt-style string (artist
   names, 4k/8k/UHD/HDR tokens, "trending on artstation"),
   surface a finding.

3. **Per-figure dimension consistency** (low-signal):
   AI generators can produce many figures of the same exact
   pixel dimensions (batch generation). When we see >= 3 images
   with identical dimensions, surface a "reviewer attention
   recommended" finding.

The detector is best-effort: if it produces zero findings on a
paper whose retraction notice names "AI generation", we mark the
paper as ``not_testable`` in the alignment. This is the honest
default.
"""
from __future__ import annotations

import json
import re

from ..contracts import Finding, ParsedDoc
from .base import DetectorResult


# ---------- Probe 1: AI-tool metadata fingerprint ----------

# Strings that appear in /Producer, /Creator, XMP packets when the
# figure was rendered by a known AI image generator. Case-insensitive
# substring match. Each entry is ``(display_name, severity)``.
#
# The C2PA / IPTC entries come from the Content Credentials
# standard (DALL-E, Firefly, Bing Image Creator all emit them).
# Verified via web search (FileShot.io / C2PA docs) on 2026-06-13.
_AI_TOOL_FINGERPRINTS: tuple[tuple[str, str], ...] = (
    ("Midjourney", "high"),
    ("Stable Diffusion", "high"),
    ("ComfyUI", "high"),
    ("DALL-E", "high"),
    ("DALL·E", "high"),
    ("Firefly", "high"),
    ("Adobe Firefly", "high"),
    ("Leonardo.AI", "medium"),
    ("Ideogram", "medium"),
    ("NovelAI", "medium"),
    ("Krea.ai", "medium"),
    # C2PA Content Credentials + IPTC DigitalSourceType markers.
    # Both are industry-standard provenance signals that DALL-E,
    # Firefly, and Bing Image Creator all emit. Verified via
    # FileShot.io / C2PA docs on 2026-06-13.
    ("trainedAlgorithmicMedia", "high"),
    ("c2pa", "medium"),
    ("content credentials", "medium"),
)

# AI-prompt-style tokens that often survive into PDF metadata when
# an author copy-pastes their prompt into the figure caption
# workflow. The literal three-asterisk regex token boundary is
# built via chr() so we never write three consecutive asterisks
# in source (some markdown parsers interpret that as bold).
_AI_PROMPT_TOKEN_RE = re.compile(
    r"\b("
    r"8k|4k|uhd|hdr|photorealistic|hyper" + chr(45) + r"realistic"
    r"|cinematic lighting|octane render|unreal engine"
    r"|trending on artstation|by greg rutkowski|by wlop"
    r")\b",
    re.IGNORECASE,
)


def _read_pdf_info_dict(path: str) -> dict[str, str]:
    """Read the ``/Info`` dictionary as a plain ``dict[str, str]``.

    Lazy-imports ``pikepdf`` so the rest of the package loads
    even if pikepdf is unavailable.
    """
    try:
        import pikepdf
    except Exception:  # noqa: BLE001
        return {}
    try:
        with pikepdf.open(path) as pdf:
            info = dict(pdf.docinfo or {})
    except Exception:  # noqa: BLE001
        return {}
    out: dict[str, str] = {}
    for k, v in info.items():
        key = str(k).lstrip("/")
        try:
            out[key] = str(v)
        except Exception:  # noqa: BLE001
            continue
    return out


def _read_xmp_packet(path: str) -> str:
    """Read the XMP packet as a single string. Empty on failure."""
    try:
        import pikepdf
    except Exception:  # noqa: BLE001
        return ""
    try:
        with pikepdf.open(path) as pdf:
            with pdf.open_metadata() as meta:
                return str(meta) or ""
    except Exception:  # noqa: BLE001
        return ""


def _probe_metadata(
    info: dict[str, str],
    xmp: str,
) -> list[Finding]:
    """Probe 1 + 2: metadata fingerprint + producer/creator anomaly.

    The detector surface is a list of findings (zero or more).
    """
    out: list[Finding] = []
    haystack_parts: list[str] = []
    for k in ("Producer", "Creator", "Author", "Title", "Subject",
              "Keywords", "Software"):
        if info.get(k):
            haystack_parts.append(info[k])
    if xmp:
        haystack_parts.append(xmp)
    haystack = " | ".join(haystack_parts).lower()
    if not haystack.strip():
        return out
    matched: list[tuple[str, str]] = []
    for tool, sev in _AI_TOOL_FINGERPRINTS:
        if tool.lower() in haystack:
            matched.append((tool, sev))
    if matched:
        sev_order = {"high": 3, "medium": 2, "low": 1}
        highest = max(matched, key=lambda t: sev_order.get(t[1], 0))[1]
        out.append(Finding.make(
            trace_id="",  # filled in by caller
            detector="ai_generated_figure",
            severity=highest,
            title=(
                f"PDF metadata mentions known AI image "
                f"generator(s): "
                f"{', '.join(sorted({t for t, _ in matched}))}"
            ),
            location="pdf_metadata",
            evidence=json.dumps({
                "matched_tools": [t for t, _ in matched],
                "info_keys_searched": [
                    k for k in (
                        "Producer", "Creator", "Author",
                        "Title", "Subject", "Keywords",
                        "Software",
                    ) if info.get(k)
                ],
                "xmp_present": bool(xmp),
            }),
        ))
    # Probe 2: AI-prompt residue in metadata.
    prompt_match = _AI_PROMPT_TOKEN_RE.search(haystack)
    if prompt_match:
        out.append(Finding.make(
            trace_id="",
            detector="ai_generated_figure",
            severity="medium",
            title=(
                "PDF metadata contains AI-prompt-style "
                f"token: {prompt_match.group(0)!r}"
            ),
            location="pdf_metadata",
            evidence=json.dumps({
                "matched_token": prompt_match.group(0),
                "context": haystack[
                    max(0, prompt_match.start() - 40):
                    prompt_match.end() + 40
                ],
            }),
        ))
    return out


# ---------- Probe 3: per-figure dimension-consistency heuristic ----------

_SAME_DIM_THRESHOLD = 3


def _probe_image_dim_consistency(
    images: list,
) -> list[Finding]:
    """Heuristic: >= 3 images with identical pixel dimensions.

    Returns at most one finding. Conservative -- legitimate
    panel-grid figures (e.g. 6 immunofluorescence panels of the
    same size) will NOT trigger this; the probe fires only when
    the figure count is high AND the dimensions cluster tightly.
    """
    out: list[Finding] = []
    if not images or len(images) < _SAME_DIM_THRESHOLD:
        return out
    dims: list[tuple[int, int]] = []
    for img in images:
        w = getattr(img, "width", None)
        h = getattr(img, "height", None)
        if isinstance(w, int) and isinstance(h, int) and w > 0 and h > 0:
            dims.append((w, h))
    if len(dims) < _SAME_DIM_THRESHOLD:
        return out
    from collections import Counter
    top_dim, top_count = Counter(dims).most_common(1)[0]
    if top_count >= _SAME_DIM_THRESHOLD:
        out.append(Finding.make(
            trace_id="",
            detector="ai_generated_figure",
            severity="low",
            title=(
                f"{top_count} images share identical pixel "
                f"dimensions ({top_dim[0]}x{top_dim[1]}) -- "
                f"consistent with batch AI generation, but also "
                f"common in legitimate panel-grid figures"
            ),
            location="images",
            evidence=json.dumps({
                "dominant_dim": list(top_dim),
                "dominant_count": top_count,
                "total_images": len(images),
                "unique_dims": len(set(dims)),
            }),
        ))
    return out


# ---------- Detector class ----------


class AIGeneratedFigureDetector:
    """Three-probe AI-figure detector. See module docstring."""

    name = "ai_generated_figure"

    def run(self, doc: ParsedDoc) -> DetectorResult:
        findings: list[Finding] = []
        path = doc.source_path
        # Probe 1 + 2.
        info = _read_pdf_info_dict(path)
        xmp = _read_xmp_packet(path)
        for f in _probe_metadata(info, xmp):
            findings.append(Finding.make(
                trace_id=doc.trace_id,
                detector=self.name,
                severity=f.severity,
                title=f.title,
                location=f.location,
                evidence=f.evidence,
            ))
        # Probe 3.
        for f in _probe_image_dim_consistency(doc.images):
            findings.append(Finding.make(
                trace_id=doc.trace_id,
                detector=self.name,
                severity=f.severity,
                title=f.title,
                location=f.location,
                evidence=f.evidence,
            ))
        return DetectorResult(
            detector=self.name,
            findings=findings,
            ok=True,
        )
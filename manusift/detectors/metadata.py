"""Metadata detector.

Flags suspicious entries in the PDF's document-info dictionary:
* Producer/Creator set to a non-academic tool (Photoshop, GIMP, MS Word
  doc-to-PDF, etc.) — common indicator of an image that was edited
  outside the paper's claimed pipeline.
* A non-empty ModDate that precedes CreationDate (file was backdated).
* Producer contains "pdfTeX"/"LaTeX" but ModDate is more than 1 day
  earlier than CreationDate.
"""
from __future__ import annotations

from ..contracts import Finding, ParsedDoc
from .base import DetectorResult


_SUSPICIOUS_PRODUCERS = (
    "photoshop",
    "gimp",
    "msword",
    "microsoft:print",
    "pdfcreator",
    "prince",
    "wkhtmltopdf",
)


def _lc(value: str | None) -> str:
    return (value or "").lower()


class MetadataDetector:
    """Inspect PDF metadata (producer, creator, dates) and
    flag entries that suggest the document was generated by
    a non-academic tool, or has suspicious creation/modification
    timestamps. Returns no findings for normal academic PDFs."""

    name = "metadata"

    def run(self, doc: ParsedDoc) -> DetectorResult:
        findings: list[Finding] = []
        meta = doc.metadata or {}
        producer = _lc(meta.get("producer"))
        creator = _lc(meta.get("creator"))
        mod = meta.get("modDate") or meta.get("moddate")
        creation = meta.get("creationDate") or meta.get("creationdate")

        for field_name, value in (("Producer", producer), ("Creator", creator)):
            for tag in _SUSPICIOUS_PRODUCERS:
                if tag in value:
                    findings.append(
                        Finding.make(
                            trace_id=doc.trace_id,
                            detector=self.name,
                            severity="medium",
                            title=f"{field_name} indicates a non-academic tool",
                            evidence=(
                                f"{field_name}='{meta.get(field_name)}' matches "
                                f"'{tag}', which is unusual for a research PDF."
                            ),
                            location="PDF / Info dictionary",
                            raw={"field": field_name, "value": meta.get(field_name)},
                        )
                    )

        if mod and creation and str(mod)[:14] < str(creation)[:14]:
            findings.append(
                Finding.make(
                    trace_id=doc.trace_id,
                    detector=self.name,
                    severity="low",
                    title="ModDate precedes CreationDate",
                    evidence=(
                        f"CreationDate='{creation}' but ModDate='{mod}' — the "
                        "file appears to have been backdated."
                    ),
                    location="PDF / Info dictionary",
                    raw={"creationDate": creation, "modDate": mod},
                )
            )

        return DetectorResult(detector=self.name, ok=True, findings=findings)
